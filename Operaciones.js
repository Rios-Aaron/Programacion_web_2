var valorA = parseFloat(prompt("Ingresa el primer valor"));
var valorB = parseFloat(prompt("Ingresa el segundo valor"));

console.log("Suma : " + (valorA + valorB));
console.log("Resta : " + (valorA - valorB));
console.log("Multiplicación : " + (valorA * valorB));
console.log("División : " + (valorA / valorB));
console.log("Módulo : " + (valorA % valorB));
